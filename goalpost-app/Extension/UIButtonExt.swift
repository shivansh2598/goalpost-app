import UIKit

extension UIButton {
    func setSelectedBtn()
    {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.8279415965, blue: 0, alpha: 1)
    }
    
    func setDeselectedBtn()
    {
        self.backgroundColor = #colorLiteral(red: 0.6519300938, green: 0.8728946447, blue: 0.6689990759, alpha: 1)
    }
}
